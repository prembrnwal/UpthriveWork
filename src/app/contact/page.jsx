function DateTimeInput({ label, ...props }) {
  let id = useId()

  return (
    <div className="relative border border-neutral-300 px-6 pt-12 pb-4 first:rounded-t-2xl last:rounded-b-2xl">
      <label
        htmlFor={id}
        className="absolute top-4 left-6 text-base/6 text-neutral-500"
      >
        {label}
      </label>

      <input
        type="datetime-local"
        id={id}
        {...props}
        min={new Date().toISOString().slice(0, 16)}
        className="block w-full bg-transparent text-neutral-500 focus:text-neutral-950 outline-none"
      />
    </div>
  )
}

